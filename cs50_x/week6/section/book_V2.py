# Simple program showcasing dictionary.

def main():
    # Creating a dictionary called book.
    book = {
        "title" : "Reverend Insanity", 
        "author" : "Gu Zhen Ren"
    }

    # Accessing the value associated with its key.
    print(f'Title: {book["title"]}')
    print(f'Author: {book["author"]}') 

main()
